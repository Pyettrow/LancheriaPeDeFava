    /**
     * Tela recebe a origem da mesma e com isso realiza um select no banco com 
     * todos os dados do produtos ou das categorias.
     * tela = 1 - Cadastro de categoria
     * tela = 2 - Cadastro de produto
     * @param tela
     */
    public FrmPesquisa(int tela) {
        this.setResizable(false);
        this.tela = tela;
        try {
            initComponents();
            this.setModal(true);
            DefaultListModel dlm = new DefaultListModel();
            
            DAL.DAO dao = new DAL.DAO<Categoria>();
            
            if(tela == 1){
                ArrayList<Categoria> lstCategorias = dao.todosRegistros(Categoria.class, 0, 0);
                jList.setToolTipText("Lista de Categorias");
                if(!lstCategorias.isEmpty()){
                    for (int i = 0; i < lstCategorias.size(); i++) {
                        dlm.add(i, lstCategorias.get(i).getIdCategoria()+"-"+lstCategorias.get(i).getDescricao());
                    }
                }
            }else if(tela == 2){
                ArrayList<Produto> lstProduto = dao.todosRegistros(Produto.class, 0, 0);
                jList.setToolTipText("Lista de Produtos");
                if(!lstProduto.isEmpty()){
                    for (int j = 0; j < lstProduto.size(); j++) {
                        dlm.add(j, lstProduto.get(j).getIdProduto()+"-"+lstProduto.get(j).getDescricao());
                    }
                }
            }
            jList.setModel(dlm);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmPesquisa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmPesquisa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

